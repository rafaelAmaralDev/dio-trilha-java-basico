public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        String meuNome = "Rafael";
        int minhaIdade = 26;
        double minhaAltura = 1.63;
        boolean estouAprendendoJava = true;
        short numeroCurto = 100;
        long numeroLongo = 10000000000L;
        float numeroDecimalCurto = 3.14f;
        char letra = 'R';

        System.out.println("Meu nome é: " + meuNome);
        System.out.println("Minha idade é: " + minhaIdade);
        System.out.println("Minha altura é: " + minhaAltura);
        System.out.println("Estou aprendendo Java: " + estouAprendendoJava);
        System.out.println("Número curto: " + numeroCurto);
        System.out.println("Número longo: " + numeroLongo);
        System.out.println("Número decimal curto: " + numeroDecimalCurto);
        System.out.println("Letra: " + letra);
    }
}
